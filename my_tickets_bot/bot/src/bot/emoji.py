HR_12 = 60 * 12  # 12 —á–∞—Å–æ–≤ –≤ –º–∏–Ω—É—Ç–∞—Ö


def get_clock_emoji(
        time: datetime.time | datetime.datetime,
) -> str:
    """–ü–æ–ª—É—á–µ–Ω–∏–µ —ç–º–æ–¥–∑–∏ —á–∞—Å–æ–≤ –≤ –∑–∞–≤–∏—Å–∏–º–æ—Å—Ç–∏ –æ—Ç –≤—Ä–µ–º–µ–Ω–∏"""

    interval = get_interval_number(time)
    return CLOCK_EMOJIS[interval]


def get_interval_number(
        time: datetime.time,
) -> int:
    """–ü–æ–ª—É—á–µ–Ω–∏–µ –Ω–æ–º–µ—Ä–∞ –∏–Ω—Ç–µ—Ä–≤–∞–ª–∞"""
    if (total_minutes := time.hour * 60 + time.minute) >= HR_12:
        total_minutes -= HR_12

    # –û–∫—Ä—É–≥–ª—è–µ–º –∫ –±–ª–∏–∂–∞–π—à–µ–º—É –∏–Ω—Ç–µ—Ä–≤–∞–ª—É
    return floor(total_minutes / 30 + 0.5)
